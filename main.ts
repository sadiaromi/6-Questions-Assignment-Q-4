function addNumbers(num1: number, num2: number): number {
    return num1 + num2;
}

let firstNumber = 4;
let secondNumber = 3;
console.log("The sum of first two numbers is:", addNumbers(firstNumber, secondNumber));

let thirdNumber = 6;
let fourthNumber = 4;
console.log("The sum of the second two numbers is:", addNumbers(thirdNumber, fourthNumber));